#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 3;
	if (nb <= 0 || nb == 2 || nb == 3)
		return (0);
	if (nb == 1)
		return (1);
	while (i * i < nb)
		i++;
	if (nb % i == 0)
		return (i);
	else
		return (0);
}
